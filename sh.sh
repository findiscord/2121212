#!/bin/bash

# Проверяем наличие аргумента (имени процесса)
if [ -z "$1" ]; then
    echo "Использование: $0 <имя_процесса>"
    exit 1
fi

# Ищем PID процесса по имени
pid=$(pgrep "$1")

# Проверяем, найден ли процесс
if [ -z "$pid" ]; then
    echo "Процесс с именем '$1' не найден."
    exit 1
fi

# Выводим информацию о процессе с использованием ps
echo "Информация о процессе $1 (PID $pid):"
ps -p "$pid" -o pid,ppid,cmd

exit 0
